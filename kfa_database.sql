);

create table if not exists public.auto_verbals(
    id bigint generated by default as identity primary key,
    auto_verbal_id text not null unique,
    image text not null,
    property_type_id bigint not null references public.properties_types(id) on delete cascade,
    bank_id bigint references public.banks(id) on delete set null,
    latitude numeric not null,
    longitude numeric not null,
    user_id uuid not null references public.users(id) on delete cascade,
    owner_name text not null,
    owner_phone text not null,
    bank_officer_name text,
    bank_officer_phone text,
    address text not null,
    created_at timestamp with time zone default now() not null
);

create or replace function public.create_vpoint_transaction_on_auto_verbal_inserted()
returns trigger as
$$
    begin
        insert into public.vpoint_transactions(type, user_id, vpoints) values (
            'auto_verbal',
            new.user_id,
            -new.price
        );
        return new;
    end; 
$$ language plpgsql;

drop trigger if exists create_vpoint_transaction_on_auto_verbal_inserted on public.auto_verbals;
create trigger create_vpoint_transaction_on_auto_verbal_inserted
    after insert on public.auto_verbals
    for each row
    execute procedure public.create_vpoint_transaction_on_auto_verbal_inserted();
